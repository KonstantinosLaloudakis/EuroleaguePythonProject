def clean_year(year_string):
    # Check if 'present' is in the string
    present = "–present" if 'present' in year_string else ""
    year_string = year_string.replace("\u2013", "-")  # Replace en dash with a standard dash

    # Match year ranges with '{{nbay|start}}' and '{{nbay|end}}' formats
    match = re.search(r'\{\{nbay\|(\d{4})\|start\|?\}\}\s*-\s*\{\{nbay\|(\d{4})\|end\|?\}\}', year_string)
    if match:
        start_year = match.group(1)
        end_year = match.group(2)
        return f"{start_year}-{end_year}{present}"

    # Match year ranges with '{{nbay|end}}' and '{{nbay|end}}' formats
    match = re.search(r'\{\{nbay\|(\d{4})\|end\|?\}\}\s*-\s*\{\{nbay\|(\d{4})\|end\|?\}\}', year_string)
    if match:
        start_year = match.group(1)
        end_year = match.group(2)
        return f"{start_year}-{end_year}{present}"

    # Match single years in the '{{nbay|}}' format
    match = re.search(r'\{\{nbay\|(\d{4})\|full=y\}\}', year_string)
    if match:
        return match.group(1) + present

    # Match single years in the '{{nbay|}}' format
    match = re.search(r'\{\{nbay\|(\d{4})\|end\}\}', year_string)
    if match:
        return match.group(1) + present

    # Match single years in the '{{nbay|}}' format
    match = re.search(r'\{\{nbay\|(\d{4})\|start\}\}', year_string)
    if match:
        return match.group(1) + present

    # Extract any year range
    match = re.search(r'(\d{4}–\d{4}|\d{4})(.*)', year_string)
    if match:
        year_range = match.group(0)  # Get the year or year range
        return year_range

    return year_string  # Return original if no match


def fetch_player_data(player_name):
    url = "https://en.wikipedia.org/w/api.php"
    params = {
        "action": "query",
        "prop": "revisions",
        "rvprop": "content",
        "format": "json",
        "titles": player_name
    }
    response = requests.get(url, params=params)
    data = response.json()
    page = next(iter(data['query']['pages'].values()))

    if 'revisions' in page:
        content = page['revisions'][0]['*']
        pattern = r'\| years\d+\s*=\s*(.+?)\s*\| team\d+\s*=\s*(?:\[\[(.+?)(?:\|.*?)?\]\]|([^\n]+))'
        career_history = re.findall(pattern, content)

        career_data = {
            clean_year(years): team_bracketed if team_bracketed else team_plain
            for years, team_bracketed, team_plain in career_history
        }
        if career_data:  # Ensure we only return non-empty career data
            return {player_name: career_data}
    return {player_name: "Data not found"}

